    /**
     * Get organization settings or default
     */
    public static function getSetting($key, $default = null)
    {
        if (!Auth::check() || !Auth::user()->organization) {
            return $default;
        }

        $settings = Auth::user()->organization->settings ?? [];
        return $settings[$key] ?? $default;
    }

    /**
     * Get organization timezone
     */
    public static function getTimezone()
    {
        return self::getSetting('timezone', config('app.timezone', 'Africa/Casablanca'));
    }

    /**
     * Get organization language
     */
    public static function getLanguage()
    {
        if (!Auth::check()) {
            return 'fr';
        }

        // First check user preference
        if (Auth::user()->language) {
            return Auth::user()->language;
        }

        // Fallback to organization setting
        return self::getSetting('language', 'fr');
    }

    /**
     * Get date format
     */
    public static function getDateFormat()
    {
        return self::getSetting('date_format', 'd/m/Y');
    }

    /**
     * Get time format
     */
    public static function getTimeFormat()
    {
        return self::getSetting('time_format', 'H:i');
    }

    /**
     * Format a date according to organization settings
     */
    public static function formatDate($date, $includeTime = false)
    {
        if (!$date) {
            return '';
        }

        $carbon = $date instanceof Carbon ? $date : Carbon::parse($date);
        $carbon->setTimezone(self::getTimezone());

        $format = self::getDateFormat();

        if ($includeTime) {
            $format .= ' ' . self::getTimeFormat();
        }

        return $carbon->format($format);
    }

    /**
     * Format a time according to organization settings
     */
    public static function formatTime($time)
    {
        if (!$time) {
            return '';
        }

        $carbon = $time instanceof Carbon ? $time : Carbon::parse($time);
        $carbon->setTimezone(self::getTimezone());

        return $carbon->format(self::getTimeFormat());
    }

